CREATE TABLE vins (
  num integer NOT NULL,
  cru varchar(80) NOT NULL,
  annee decimal(4,0) NOT NULL,
  degre decimal(4,2) NOT NULL,
  PRIMARY KEY  (num)
);

DROP TABLE IF EXISTS producteurs;
CREATE TABLE producteurs (
  num integer  NOT NULL,
  nom varchar(80) NOT NULL,
  prenom varchar(80) default NULL,
  region varchar(80) default NULL,
  PRIMARY KEY  (num)
);

DROP TABLE IF EXISTS recoltes;
CREATE TABLE recoltes (
  nprod integer  NOT NULL ,
  nvin integer  NOT NULL,
  quantite decimal(4,0) default NULL,
  PRIMARY KEY  (nprod,nvin),
  CONSTRAINT FK_recolte_1 FOREIGN KEY (nprod) REFERENCES producteurs (num),
  CONSTRAINT FK_recolte_2 FOREIGN KEY (nvin) REFERENCES vins (num)
);
